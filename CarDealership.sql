-- TABLE 3
-- INVENTORY(TRACK WHICH DEALERSHIP HAS THE VEHICLE)
CREATE TABLE Inventory(
DealerShipID INT NOT NULL,
VIN INT NOT NULL,
FOREIGN KEY (DealerShipID) REFERENCES Dealership(DealerShipID),
FOREIGN KEY (VIN) REFERENCES Vehicles(VIN)
