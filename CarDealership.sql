-- TABLE 1
-- DEALERSHIP

CREATE TABLE Dealership (
DealerShipID INT PRIMARY KEY AUTO_INCREMENT ,
Name VARCHAR(50)  NOT NULL,
Address VARCHAR(50) NOT NULL,
Phone VARCHAR(50) NOT NULL
);

