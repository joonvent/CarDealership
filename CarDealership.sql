-- TABLE 2 
-- VEHICLES

CREATE TABLE Vehicles(
VIN INT PRIMARY KEY NOT NULL,
Make VARCHAR(20) NOT NULL,
Model VARCHAR(15) NOT NULL,
Year INT NOT NULL,
Price Numeric(10,2) NOT NULL,
Sold BIT
);

